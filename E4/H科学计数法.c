#include <stdio.h>
#include <string.h>
int main()
{
	int m=50,i,dian,xiao,len,e,q;
	char n[20]={0};
	scanf("%s",&n);
	len=strlen(n);
	i=0;
	while(n[i]!='.')
	{
		i++;
	}
	dian=i;
	if(dian==len)//没有小数点 
	{
		for(i=0;i<len;i++)
		{
			if(n[i]!='0')
			{ 
				m=i;
				break;
			}
		}
		if(i==len)
		{
			printf("0");
		}
		else
		{
			if(m==len-1)//只有一位数 
				printf("%c",n[m]);
			else
			{
					for(i=len-1;i>m;i--)
				{
					if(n[i]!='0')
					break;
				}
				q=i;
				if(m==q)//只有一位有效数字，后面全是0 
				{
					e=len-m-1;
					printf("%cE%d",n[m],e);
				}
				else
				{
					e=len-m-1;
					printf("%c.",n[m]);
					for(i=m+1;i<=q;i++)
						printf("%c",n[i]);
					printf("E%d",e);
				}
			}
		}
	}
	else
	{
		for(i=0;i<dian;i++)
		{
			if(n[i]!='0')
			{
				m=i;
				break;
			}
		}
		i=len-1;
		while(n[i]=='0')
		i--;
		xiao=i;
		if(m==50&&xiao==dian)//0
		printf("0");
		else if(m==50)//只有小数 
		{
			i=dian+1;
			while(n[i]=='0')
			i++;
			q=i;//第一位不为0的数 
			e=q-dian;
			if(q==xiao)//只有一位有效数字 
			{
				printf("%cE-%d",n[xiao],e);	
			}
			else
			{
				printf("%c.",n[q]);
				for(i=q+1;i<=xiao;i++)
				printf("%c",n[i]);
				printf("E-%d",e);
			}
		}
		else if(xiao==dian)//只有整数 
		{
			if(dian-m==1)
			{
				printf("%c",n[m]);
			}
			else
			{
				for(i=dian-1;n[i]=='0';i--)
				{
					;
				}
				e=dian-m-1;
				q=i;//最后一位有效数字
				if(q==m)
				{
					printf("%cE%d",n[m],e);
				 } 
				else
				{
					printf("%c.",n[m]);
					for(i=m+1;i<=q;i++)
					{
						printf("%c",n[i]);
					}
					printf("E%d",e);
				}
			}
		}
		else//小数整数都有 
		{
			e=dian-m-1;
			printf("%c.",n[m]);
			for(i=m+1;i<dian;i++)
			printf("%c",n[i]);
			for(i=dian+1;i<=xiao;i++)
			{
				printf("%c",n[i]);
			}
			if(e==0)
				{
					;
				}
			else
				printf("E%d",e);
		}
	}
 	return 0;
}
